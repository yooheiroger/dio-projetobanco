nome = "Yoohei Roger Haga"
 
print (nome[0]) #Vai pegar a primeira casa da string Y

print (nome[:9])#Vai pegar a nona casa da string (a ultima casa é - 1)

print (nome[10:])# vAI PEGAR apartir da 10 casa

print (nome[10:13])#vai pegar da casa 10 ate a 13 (a ultima casa é -1 )

print (nome[5:13:2])# pega da casa 5 ate a 13 de 2 em 2

print (nome[:]) #todas as casas

print (nome[::-1])#espelha a string




